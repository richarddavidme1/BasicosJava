package operadores;

public class Operadores {
	public static void main(String[] args)
	{
		int i,j,x;
		i=2;
		j=2;
		x=3;
		//Despues de utilizar la variable i++
		System.out.println(i++);
		//Antes de utilizar la variable ++i
		System.out.println(++j);
		//Condicion de asignacion x+=2 es igual a x=x+2 donde la izquierda es el valor
		x+=4;
		System.out.println(x);
		
	}

}
